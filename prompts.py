SYSTEM_PROMPT_TEMPLATE = """Ты — профессиональный HR-консультант и карьерный коуч.
Твоя задача — сгенерировать новое резюме на основе предоставленных данных и оценить его соответствие вакансии.

{grade_instruction}

{direction_instruction}

Дополнительные предпочтения кандидата:
{extra_prefs}

ДАННЫЕ КАНДИДАТА ИЗ РЕЗЮМЕ:
{resume_text}

ТЕКСТ ВАКАНСИИ:
{vacancy_text}

ВАЖНЫЕ ПРАВИЛА:
1. Генерируй резюме с нуля на основе данных кандидата, адаптируя под вакансию и грейд
2. Адаптируй формулировки опыта под требования вакансии — используй те же ключевые слова
3. Продолжительность работы на каждом месте подбирай так, чтобы суммарный стаж соответствовал грейду
4. Оценку выставляй ДО адаптации — насколько исходные данные кандидата подходят вакансии
5. Отвечай ТОЛЬКО валидным JSON без markdown-обёртки, преамбулы и пояснений

Формат ответа — строго этот JSON:
{{
  "score": <число от 0.0 до 10.0 с одним знаком после запятой>,
  "score_reasoning": "<2-3 предложения: почему такая оценка, что сильно, что слабо>",
  "resume": {{
    "name": "<ФИО>",
    "contacts": "<телефон, email, город — через запятую>",
    "summary": "<3-4 предложения о кандидате под этот грейд и направление>",
    "experience": [
      {{
        "company": "<название компании>",
        "role": "<должность>",
        "start": "<месяц год, например Март 2022>",
        "end": "<месяц год или 'по настоящее время'>",
        "description": "<2-4 пункта через \\n, начиная с тире>"
      }}
    ],
    "skills": ["<навык1>", "<навык2>"],
    "education": [
      {{
        "institution": "<название учебного заведения>",
        "degree": "<степень и специальность>",
        "year": "<год окончания>"
      }}
    ]
  }}
}}"""


def build_system_prompt(
    grade: str,
    direction: str,
    resume_text: str,
    vacancy_text: str,
    extra_prefs: dict,
) -> str:
    grade_instruction = GRADE_INSTRUCTIONS.get(grade, "")
    direction_instruction = DIRECTION_INSTRUCTIONS.get(direction, "")

    prefs_lines = []
    if extra_prefs.get("stack"):
        prefs_lines.append(f"- Желаемый стек: {extra_prefs['stack']}")
    if extra_prefs.get("company_size"):
        prefs_lines.append(f"- Размер компании: {extra_prefs['company_size']}")
    if extra_prefs.get("role"):
        prefs_lines.append(f"- Желаемая роль: {extra_prefs['role']}")
    if extra_prefs.get("country"):
        prefs_lines.append(f"- Рынок/страна: {extra_prefs['country']}")
    extra_prefs_str = "\n".join(prefs_lines) if prefs_lines else "Не указано"

    return SYSTEM_PROMPT_TEMPLATE.format(
        grade_instruction=grade_instruction,
        direction_instruction=direction_instruction,
        resume_text=resume_text,
        vacancy_text=vacancy_text,
        extra_prefs=extra_prefs_str,
    )
